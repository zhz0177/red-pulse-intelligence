// 自动建表 + 种子数据（首次调用或表为空时补全）
// ------------------------------------------------------------
function choiceInsertSeedRows(PDO $pdo) {
    $rows = choiceSeedStories();
    $stmt = $pdo->prepare('INSERT INTO choice_stories (martyr_id, title, summary, intro, steps_json, correct_answers, ending_title, ending_content) VALUES (?,?,?,?,?,?,?,?)');
    foreach ($rows as $r) {
        $stmt->execute([
            $r['martyr_id'],
            $r['title'],
            $r['summary'],
            $r['intro'],
            json_encode($r['steps'], JSON_UNESCAPED_UNICODE),
            json_encode($r['correct_answers'], JSON_UNESCAPED_UNICODE),
            $r['ending_title'],
            $r['ending_content'],
        ]);
    }
}

function choiceEnsureSchema(PDO $pdo) {
    $tableExists = true;
    try {
        $pdo->query('SELECT 1 FROM choice_stories LIMIT 1');
    } catch (Exception $e) {
        $tableExists = false;
    }

    if (!$tableExists) {
        $pdo->exec("
            CREATE TABLE IF NOT EXISTS `choice_stories` (
                `id` INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
                `martyr_id` INT UNSIGNED NOT NULL,
                `title` VARCHAR(200) NOT NULL COMMENT '关卡标题',
                `summary` VARCHAR(600) NOT NULL DEFAULT '' COMMENT '列表摘要',
                `intro` TEXT NOT NULL COMMENT '背景导语',
                `steps_json` JSON NOT NULL COMMENT '剧情步骤（含选项文案，不含正确答案）',
                `correct_answers` JSON NOT NULL COMMENT '每步正确选项 A/B/C',
                `ending_title` VARCHAR(200) NOT NULL DEFAULT '',
                `ending_content` TEXT NOT NULL COMMENT '真实历史结局',
                `created_at` DATETIME DEFAULT CURRENT_TIMESTAMP,
                UNIQUE KEY `uk_martyr` (`martyr_id`),
                CONSTRAINT `fk_choice_martyr` FOREIGN KEY (`martyr_id`) REFERENCES `martyrs`(`id`) ON DELETE CASCADE
            ) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

            CREATE TABLE IF NOT EXISTS `choice_records` (
                `id` INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
                `user_id` INT UNSIGNED NOT NULL,
                `story_id` INT UNSIGNED NOT NULL,
                `answers_json` JSON NOT NULL COMMENT '用户每步所选 A/B/C',
                `all_correct` TINYINT(1) NOT NULL DEFAULT 0,
                `reason` TEXT COMMENT '错误时填写的原因',
                `created_at` DATETIME DEFAULT CURRENT_TIMESTAMP,
                KEY `idx_user` (`user_id`),
                KEY `idx_story` (`story_id`),
                KEY `idx_created` (`created_at`),
                CONSTRAINT `fk_choice_rec_user` FOREIGN KEY (`user_id`) REFERENCES `users`(`id`) ON DELETE CASCADE,
                CONSTRAINT `fk_choice_rec_story` FOREIGN KEY (`story_id`) REFERENCES `choice_stories`(`id`) ON DELETE CASCADE
            ) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;
        ");
        choiceInsertSeedRows($pdo);
        return;
    }

    // 表已存在但曾建表失败未写入种子、或被人清空时，补插 31 条剧情
    $n = (int)$pdo->query('SELECT COUNT(*) FROM choice_stories')->fetchColumn();
    if ($n === 0) {
        choiceInsertSeedRows($pdo);
    }
}

