// ------------------------------------------------------------
// 辅助：剥离正确答案后返回剧情
// ------------------------------------------------------------
function choiceStoryPublicRow(array $row) {
    $steps = json_decode($row['steps_json'], true) ?: [];
    return [
        'id' => (int)$row['id'],
        'martyr_id' => (int)$row['martyr_id'],
        'title' => $row['title'],
        'summary' => $row['summary'],
        'intro' => $row['intro'],
        'steps' => $steps,
        'step_count' => count($steps),
    ];
}

choiceEnsureSchema($pdo);

$action = $_GET['action'] ?? '';

switch ($action) {

    // === 用户端：剧情关卡列表（关联英烈姓名）===
    case 'list':
        $user = checkLogin();
        $stmt = $pdo->query('
            SELECT cs.id, cs.martyr_id, cs.title, cs.summary, m.name AS martyr_name
            FROM choice_stories cs
            JOIN martyrs m ON m.id = cs.martyr_id
            ORDER BY cs.martyr_id ASC
        ');
        ok($stmt->fetchAll());
        break;

    // === 用户端：单关剧情（不含正确答案与历史结局全文，防剧透可选）===
    case 'story':
        $user = checkLogin();
        $id = (int)($_GET['id'] ?? 0);
        $martyrId = (int)($_GET['martyr_id'] ?? 0);
        if ($id) {
            $stmt = $pdo->prepare('SELECT * FROM choice_stories WHERE id = ?');
            $stmt->execute([$id]);
        } elseif ($martyrId) {
            $stmt = $pdo->prepare('SELECT * FROM choice_stories WHERE martyr_id = ?');
            $stmt->execute([$martyrId]);
        } else {
            fail('请提供 id 或 martyr_id');
        }
        $row = $stmt->fetch();
        if (!$row) fail('剧情不存在', 404);
        $nameStmt = $pdo->prepare('SELECT name FROM martyrs WHERE id = ?');
        $nameStmt->execute([$row['martyr_id']]);
        $mn = $nameStmt->fetchColumn();
        $pub = choiceStoryPublicRow($row);
        $pub['martyr_name'] = $mn ?: '';
        // 用户进行中的剧情：不返回历史结局，待提交后由 choose 返回
        ok($pub);
        break;

    // === 用户端：提交选择 ===
    case 'choose':
        $user = checkLogin();
        $data = input();
        $storyId = (int)($data['story_id'] ?? $data['id'] ?? 0);
        $answers = $data['answers'] ?? null;
        if (!$storyId || !is_array($answers)) fail('参数错误：需要 story_id 与 answers 数组');

        $stmt = $pdo->prepare('SELECT * FROM choice_stories WHERE id = ?');
        $stmt->execute([$storyId]);
        $story = $stmt->fetch();
        if (!$story) fail('剧情不存在', 404);

        $correct = json_decode($story['correct_answers'], true);
        if (!is_array($correct)) fail('剧情数据异常', 500);
        $steps = json_decode($story['steps_json'], true);
        if (!is_array($steps) || count($steps) !== count($correct)) fail('剧情数据异常', 500);

        if (count($answers) !== count($correct)) fail('选项步数与剧情不一致');

        $norm = [];
        foreach ($answers as $a) {
            $L = strtoupper(trim((string)$a));
            if (!in_array($L, ['A', 'B', 'C'], true)) fail('答案必须是 A/B/C');
            $norm[] = $L;
        }

        $allCorrect = true;
        for ($i = 0; $i < count($correct); $i++) {
            if (strtoupper($correct[$i]) !== $norm[$i]) {
                $allCorrect = false;
                break;
            }
        }

        $reason = trim((string)($data['reason'] ?? ''));
        // 答错但未写原因：返回 400，并在 data 中带史实答案与结局，供前端展示后再二次提交
        if (!$allCorrect && mb_strlen($reason) < 4) {
            http_response_code(400);
            echo json_encode([
                'code' => 400,
                'message' => '选择有误，请简要填写您的思考或认识（不少于4个字）',
                'data' => [
                    'need_reason' => true,
                    'all_correct' => false,
                    'correct_answers' => array_map('strtoupper', array_map('strval', $correct)),
                    'your_answers' => $norm,
                    'ending_title' => $story['ending_title'],
                    'ending_content' => $story['ending_content'],
                ],
            ], JSON_UNESCAPED_UNICODE);
            exit;
        }

        $ins = $pdo->prepare('INSERT INTO choice_records (user_id, story_id, answers_json, all_correct, reason) VALUES (?,?,?,?,?)');
        $ins->execute([
            $user['id'],
            $storyId,
            json_encode($norm, JSON_UNESCAPED_UNICODE),
            $allCorrect ? 1 : 0,
            $allCorrect ? null : $reason,
        ]);

        ok([
            'all_correct' => $allCorrect,
            'correct_answers' => $correct,
            'your_answers' => $norm,
            'ending_title' => $story['ending_title'],
            'ending_content' => $story['ending_content'],
            'record_id' => (int)$pdo->lastInsertId(),
        ], $allCorrect ? '与历史抉择一致，向英烈致敬' : '已记录您的思考，请铭记真实历史');
        break;

    // === 用户端：我的抉择记录 ===
    case 'my':
        $user = checkLogin();
        $stmt = $pdo->prepare('
            SELECT cr.id, cr.story_id, cr.answers_json, cr.all_correct, cr.reason, cr.created_at,
                   cs.title AS story_title, cs.martyr_id, m.name AS martyr_name
            FROM choice_records cr
            JOIN choice_stories cs ON cs.id = cr.story_id
            JOIN martyrs m ON m.id = cs.martyr_id
            WHERE cr.user_id = ?
            ORDER BY cr.created_at DESC
            LIMIT 500
        ');
        $stmt->execute([$user['id']]);
        $rows = $stmt->fetchAll();
        foreach ($rows as &$r) {
            $r['id'] = (int)$r['id'];
            $r['story_id'] = (int)$r['story_id'];
            $r['martyr_id'] = (int)$r['martyr_id'];
            $r['all_correct'] = (bool)(int)$r['all_correct'];
            $r['answers'] = json_decode($r['answers_json'], true);
            unset($r['answers_json']);
        }
        ok($rows);
        break;

    // === 管理端：小英雄反馈 · 全部记录（仅 admin）===
    case 'records':
        $user = checkLogin();
        if ($user['role'] !== 'admin') fail('无权限', 403);

        $username = trim($_GET['username'] ?? '');
        $martyrId = (int)($_GET['martyr_id'] ?? 0);
        $storyId = (int)($_GET['story_id'] ?? 0);
        $correct = $_GET['all_correct'] ?? '';
        $from = trim($_GET['from'] ?? '');
        $to = trim($_GET['to'] ?? '');

        $sql = '
            SELECT cr.id, cr.user_id, u.username, cr.story_id, cs.title AS story_title,
                   cs.martyr_id, m.name AS martyr_name, cr.answers_json, cr.all_correct,
                   cr.reason, cr.created_at
            FROM choice_records cr
            JOIN users u ON u.id = cr.user_id
            JOIN choice_stories cs ON cs.id = cr.story_id
            JOIN martyrs m ON m.id = cs.martyr_id
            WHERE 1=1
        ';
        $args = [];
        if ($username !== '') {
            $sql .= ' AND u.username LIKE ?';
            $args[] = '%' . $username . '%';
        }
        if ($martyrId > 0) {
            $sql .= ' AND cs.martyr_id = ?';
            $args[] = $martyrId;
        }
        if ($storyId > 0) {
            $sql .= ' AND cr.story_id = ?';
            $args[] = $storyId;
        }
        if ($correct === '1' || $correct === '0') {
            $sql .= ' AND cr.all_correct = ?';
            $args[] = (int)$correct;
        }
        if ($from !== '') {
            $sql .= ' AND cr.created_at >= ?';
            $args[] = $from . ' 00:00:00';
        }
        if ($to !== '') {
            $sql .= ' AND cr.created_at <= ?';
            $args[] = $to . ' 23:59:59';
        }
        $sql .= ' ORDER BY cr.created_at DESC LIMIT 2000';

        $stmt = $pdo->prepare($sql);
        $stmt->execute($args);
        $rows = $stmt->fetchAll();
        foreach ($rows as &$r) {
            $r['id'] = (int)$r['id'];
            $r['user_id'] = (int)$r['user_id'];
            $r['story_id'] = (int)$r['story_id'];
            $r['martyr_id'] = (int)$r['martyr_id'];
            $r['all_correct'] = (bool)(int)$r['all_correct'];
            $r['answers'] = json_decode($r['answers_json'], true);
            unset($r['answers_json']);
        }
        ok($rows);
        break;

    default:
        fail('未知操作');
}
